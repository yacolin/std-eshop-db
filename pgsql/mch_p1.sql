    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE mch_merchant_withdrawals IS '商家提现申请表';
COMMENT ON COLUMN mch_merchant_withdrawals.withdraw_no IS '提现单号';
COMMENT ON COLUMN mch_merchant_withdrawals.amount IS '提现金额（分）';
COMMENT ON COLUMN mch_merchant_withdrawals.bank_account_id IS '结算账户ID';
COMMENT ON COLUMN mch_merchant_withdrawals.status IS '0-待审核 1-审核通过 2-已打款 3-拒绝';
COMMENT ON COLUMN mch_merchant_withdrawals.audit_remark IS '审批备注';
COMMENT ON COLUMN mch_merchant_withdrawals.applied_at IS '申请时间';
COMMENT ON COLUMN mch_merchant_withdrawals.approved_at IS '审批时间';
COMMENT ON COLUMN mch_merchant_withdrawals.paid_at IS '打款时间';


CREATE TABLE mch_merchant_settlement_logs (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    merchant_id bigint NOT NULL,
    settlement_no varchar(32) NOT NULL,
    settlement_cycle varchar(20),
    total_amount bigint NOT NULL DEFAULT 0,
    commission_amount bigint NOT NULL DEFAULT 0,
    settlement_amount bigint NOT NULL DEFAULT 0,
    status smallint DEFAULT 0,
    settled_at timestamp(3),
    paid_at timestamp(3),
    remark varchar(500),
    created_at timestamp(3) DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(3) DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (settlement_no)
);

CREATE INDEX idx_mch_merchant_settlement_logs_merchant ON mch_merchant_settlement_logs (merchant_id);
CREATE INDEX idx_mch_merchant_settlement_logs_status ON mch_merchant_settlement_logs (status);
CREATE INDEX idx_mch_merchant_settlement_logs_active ON mch_merchant_settlement_logs (merchant_id, status)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_mch_merchant_settlement_logs_updated_at
    BEFORE UPDATE ON mch_merchant_settlement_logs
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE mch_merchant_settlement_logs IS '商家结算流水表';
COMMENT ON COLUMN mch_merchant_settlement_logs.settlement_no IS '结算单号';
COMMENT ON COLUMN mch_merchant_settlement_logs.settlement_cycle IS '结算周期（如 2026-07-01~2026-07-15）';
COMMENT ON COLUMN mch_merchant_settlement_logs.total_amount IS '期内总金额（分）';
COMMENT ON COLUMN mch_merchant_settlement_logs.commission_amount IS '平台佣金（分）';
COMMENT ON COLUMN mch_merchant_settlement_logs.settlement_amount IS '应结算金额（分）';
COMMENT ON COLUMN mch_merchant_settlement_logs.status IS '0-待结算 1-已结算 2-已打款';
COMMENT ON COLUMN mch_merchant_settlement_logs.settled_at IS '结算时间';
COMMENT ON COLUMN mch_merchant_settlement_logs.paid_at IS '打款时间';
COMMENT ON COLUMN mch_merchant_settlement_logs.remark IS '备注';


CREATE TABLE mch_merchant_role_permissions (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    merchant_id bigint NOT NULL,
    role_id bigint NOT NULL,
    permission_name varchar(100) NOT NULL,
    created_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (role_id, permission_name)
);

CREATE INDEX idx_mch_merchant_role_permissions_merchant ON mch_merchant_role_permissions (merchant_id)
    WHERE deleted_at IS NULL;
CREATE INDEX idx_mch_merchant_role_permissions_permission ON mch_merchant_role_permissions (permission_name);

COMMENT ON TABLE mch_merchant_role_permissions IS '商家角色-权限关联表（基于平台权限标识）';
COMMENT ON COLUMN mch_merchant_role_permissions.role_id IS '角色ID（关联 mch_merchant_roles.id）';
COMMENT ON COLUMN mch_merchant_role_permissions.permission_name IS '权限标识（对应 sys_permissions.name）';
