CREATE TABLE mch_merchant_users (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    merchant_id bigint NOT NULL,
    staff_id bigint NOT NULL,
    role_id bigint NOT NULL DEFAULT 0,
    status smallint NOT NULL DEFAULT 1,
    invited_at timestamp(3) DEFAULT NULL,
    last_login_at timestamp(3) DEFAULT NULL,
    created_at timestamp(3) DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(3) DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (merchant_id, staff_id)
);

CREATE INDEX idx_mch_merchant_users_merchant_status ON mch_merchant_users (merchant_id, status)
    WHERE deleted_at IS NULL;
CREATE INDEX idx_mch_merchant_users_staff_id ON mch_merchant_users (staff_id)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_mch_merchant_users_updated_at
    BEFORE UPDATE ON mch_merchant_users
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE mch_merchant_users IS '商家-员工关联表（B端员工通过此表绑定商户）';
COMMENT ON COLUMN mch_merchant_users.staff_id IS '员工ID（关联 sys_staff.id）';
COMMENT ON COLUMN mch_merchant_users.role_id IS '商家角色ID（关联 mch_merchant_roles.id，与平台RBAC隔离）';
COMMENT ON COLUMN mch_merchant_users.status IS '1-正常 2-禁用';
COMMENT ON COLUMN mch_merchant_users.invited_at IS '邀请时间';
COMMENT ON COLUMN mch_merchant_users.last_login_at IS '最后登录时间';


CREATE TABLE mch_merchant_balances (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    merchant_id bigint NOT NULL,
    available_balance bigint NOT NULL DEFAULT 0,
    freeze_balance bigint NOT NULL DEFAULT 0,
    currency varchar(10) NOT NULL DEFAULT 'CNY',
    version bigint NOT NULL DEFAULT 0,
    created_at timestamp(3) DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(3) DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (merchant_id, currency)
);

CREATE INDEX idx_mch_merchant_balances_active ON mch_merchant_balances (merchant_id)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_mch_merchant_balances_updated_at
    BEFORE UPDATE ON mch_merchant_balances
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE mch_merchant_balances IS '商家资金余额表';
COMMENT ON COLUMN mch_merchant_balances.available_balance IS '可提现余额（分）';
COMMENT ON COLUMN mch_merchant_balances.freeze_balance IS '冻结余额（分）';
COMMENT ON COLUMN mch_merchant_balances.currency IS '币种';
COMMENT ON COLUMN mch_merchant_balances.version IS '版本号（并发控制）';


