CREATE INDEX idx_mch_merchant_withdrawals_merchant_status ON mch_merchant_withdrawals (merchant_id, status)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_mch_merchant_withdrawals_updated_at
