CREATE TABLE usr_login_histories (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    user_id bigint NOT NULL,
    login_ip varchar(50) NOT NULL DEFAULT '',
    login_device varchar(100) NOT NULL DEFAULT '',
    login_location varchar(100) NOT NULL DEFAULT '',
    login_method login_method NOT NULL DEFAULT 'password',
    login_status smallint NOT NULL DEFAULT 1,
    failure_reason varchar(100) NOT NULL DEFAULT '',
    created_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE INDEX idx_usr_login_histories_user_id ON usr_login_histories (user_id);
CREATE INDEX idx_usr_login_histories_time_brin ON usr_login_histories USING BRIN (created_at)
    WITH (pages_per_range = 32);

COMMENT ON TABLE usr_login_histories IS 'C端消费者登录历史表';
COMMENT ON COLUMN usr_login_histories.login_ip IS '登录IP';
COMMENT ON COLUMN usr_login_histories.login_device IS '登录设备信息（UA）';
COMMENT ON COLUMN usr_login_histories.login_location IS '登录地点';
COMMENT ON COLUMN usr_login_histories.login_method IS '登录方式：password/sms/oauth';
COMMENT ON COLUMN usr_login_histories.login_status IS '登录结果：1-成功 0-失败';
COMMENT ON COLUMN usr_login_histories.failure_reason IS '失败原因';
