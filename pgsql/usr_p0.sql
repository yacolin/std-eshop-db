CREATE TABLE usr_users (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    username varchar(50) DEFAULT NULL,
    password_hash varchar(255) NOT NULL DEFAULT '',
    email varchar(100) DEFAULT NULL,
    email_verified smallint NOT NULL DEFAULT 0,
    phone varchar(20) DEFAULT NULL,
    phone_verified smallint NOT NULL DEFAULT 0,
    avatar varchar(512) NOT NULL DEFAULT '',
    nickname varchar(50) NOT NULL DEFAULT '',
    status user_status NOT NULL DEFAULT 'active',
    register_ip varchar(50) NOT NULL DEFAULT '',
    register_source register_source NOT NULL DEFAULT 'web',
    last_login_ip varchar(50) NOT NULL DEFAULT '',
    last_login_at timestamp(3) DEFAULT NULL,
    created_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (username),
    UNIQUE (email),
    UNIQUE (phone)
);

-- 部分索引：只索引未删除记录
CREATE INDEX idx_usr_users_active ON usr_users (status, id)
    WHERE deleted_at IS NULL;
CREATE INDEX idx_usr_users_status ON usr_users (status);

CREATE TRIGGER trg_usr_users_updated_at
    BEFORE UPDATE ON usr_users
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE usr_users IS '用户账户主表';
COMMENT ON COLUMN usr_users.username IS '用户名（唯一，NULL表示未设置）';
COMMENT ON COLUMN usr_users.password_hash IS 'bcrypt 密码哈希';
COMMENT ON COLUMN usr_users.email IS '邮箱（唯一，NULL表示未绑定）';
COMMENT ON COLUMN usr_users.email_verified IS '邮箱是否已验证';
COMMENT ON COLUMN usr_users.phone IS '手机号（唯一，NULL表示未绑定）';
COMMENT ON COLUMN usr_users.phone_verified IS '手机号是否已验证';
COMMENT ON COLUMN usr_users.avatar IS '头像URL';
COMMENT ON COLUMN usr_users.nickname IS '昵称';
COMMENT ON COLUMN usr_users.status IS '状态：active-正常 disabled-禁用 frozen-冻结';
COMMENT ON COLUMN usr_users.register_ip IS '注册IP';
COMMENT ON COLUMN usr_users.register_source IS '注册来源：web/ios/android/admin';
COMMENT ON COLUMN usr_users.last_login_ip IS '最后登录IP';
COMMENT ON COLUMN usr_users.last_login_at IS '最后登录时间';


