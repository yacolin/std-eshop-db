COMMENT ON COLUMN mch_merchants.business_scope IS '经营范围';
COMMENT ON COLUMN mch_merchants.business_years IS '经营年限（入驻年限）';
COMMENT ON COLUMN mch_merchants.contact_person IS '主要联系人';
COMMENT ON COLUMN mch_merchants.contact_phone IS '联系电话';
COMMENT ON COLUMN mch_merchants.contact_email IS '联系邮箱';
COMMENT ON COLUMN mch_merchants.logo_url IS '店铺Logo';
