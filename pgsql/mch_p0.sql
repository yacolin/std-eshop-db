CREATE TABLE mch_merchants (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    merchant_name varchar(100) NOT NULL,
    merchant_code varchar(50) NOT NULL,
    merchant_type smallint DEFAULT 1,
    merchant_level smallint DEFAULT 1,
    business_scope varchar(200),
    business_years int DEFAULT 0,
    contact_person varchar(50),
    contact_phone varchar(20),
    contact_email varchar(100),
    logo_url varchar(500),
    banner_url varchar(500),
    shop_description text,
    status smallint DEFAULT 0,
    audit_status smallint DEFAULT 0,
    audit_reason varchar(200),
    audited_at timestamp(3),
    frozen_reason varchar(200),
    commission_rate bigint NOT NULL DEFAULT 0,
    settlement_cycle smallint DEFAULT 1,
    total_orders int DEFAULT 0,
    total_sales bigint NOT NULL DEFAULT 0,
    avg_rating decimal(2,1) DEFAULT 0.0,
    product_count int DEFAULT 0,
    settled_at timestamp(3),
    expire_at timestamp(3),
    created_by bigint,
    updated_by bigint,
    created_at timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (merchant_code)
);

CREATE INDEX idx_mch_merchants_name ON mch_merchants (merchant_name);
CREATE INDEX idx_mch_merchants_code ON mch_merchants (merchant_code);
CREATE INDEX idx_mch_merchants_status_level ON mch_merchants (status, merchant_level);
CREATE INDEX idx_mch_merchants_settled ON mch_merchants (settled_at);

CREATE TRIGGER trg_mch_merchants_updated_at
    BEFORE UPDATE ON mch_merchants
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE mch_merchants IS '商家主表';
COMMENT ON COLUMN mch_merchants.merchant_name IS '商家名称（店铺名）';
