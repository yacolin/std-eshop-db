COMMENT ON COLUMN mch_merchants.shop_description IS '店铺简介';
COMMENT ON COLUMN mch_merchants.status IS '0-待审核 1-正常 2-冻结 3-已注销';
COMMENT ON COLUMN mch_merchants.audit_status IS '0-待审核 1-审核通过 2-审核拒绝';
