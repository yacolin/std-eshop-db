COMMENT ON COLUMN tx_payments.amount IS '支付金额（分）';
COMMENT ON COLUMN tx_payments.payment_method IS '支付方式：wechat-微信 alipay-支付宝 wallet-余额';
COMMENT ON COLUMN tx_payments.channel IS '支付渠道（如 wechat_native-微信 native alipay_page-支付宝页面）';
COMMENT ON COLUMN tx_payments.trade_type IS '交易类型：native-jsapi-app-h5-page';
COMMENT ON COLUMN tx_payments.transaction_id IS '支付渠道交易号（微信/支付宝订单号，用于对账）';
