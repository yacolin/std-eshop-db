CREATE TABLE tx_payments (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    payment_no varchar(32) NOT NULL,
    order_no varchar(32) NOT NULL,
    order_id bigint NOT NULL,
    merchant_id bigint NOT NULL DEFAULT 0,
    order_type varchar(20) NOT NULL DEFAULT 'order',
    amount bigint NOT NULL,
    currency varchar(10) NOT NULL DEFAULT 'CNY',
    payment_method varchar(32) NOT NULL,
    channel varchar(32) DEFAULT '',
    trade_type varchar(32) DEFAULT '',
    transaction_id varchar(128) DEFAULT NULL,
    idempotency_key varchar(64) NOT NULL,
    status varchar(20) NOT NULL DEFAULT 'pending',
    failure_reason varchar(500) DEFAULT '',
    client_ip varchar(50) DEFAULT '',
    expire_at timestamp(3) DEFAULT NULL,
    paid_at timestamp(3) DEFAULT NULL,
    notify_at timestamp(3) DEFAULT NULL,
    channel_response jsonb DEFAULT NULL,
    created_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (payment_no),
    UNIQUE (idempotency_key),
    UNIQUE (transaction_id),
    CONSTRAINT chk_pay_amount_positive CHECK (amount > 0)
);

CREATE INDEX idx_tx_payments_order_no ON tx_payments (order_no);
CREATE INDEX idx_tx_payments_status_expire ON tx_payments (status, expire_at);

CREATE TRIGGER trg_tx_payments_updated_at
    BEFORE UPDATE ON tx_payments
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

COMMENT ON TABLE tx_payments IS '支付单表';
COMMENT ON COLUMN tx_payments.payment_no IS '支付单号（业务唯一键）';
COMMENT ON COLUMN tx_payments.order_no IS '关联订单号';
COMMENT ON COLUMN tx_payments.order_id IS '关联 tx_orders.id';
COMMENT ON COLUMN tx_payments.merchant_id IS '所属商家ID';
