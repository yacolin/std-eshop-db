CREATE TABLE tx_payment_logs (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    payment_id bigint NOT NULL,
    payment_no varchar(32) NOT NULL,
    channel varchar(32) DEFAULT '',
    transaction_id varchar(128) DEFAULT '',
    action varchar(30) NOT NULL,
    request_body text,
    response_body text,
    status varchar(20) DEFAULT '',
    created_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE INDEX idx_tx_payment_logs_payment_id ON tx_payment_logs (payment_id);
CREATE INDEX idx_tx_payment_logs_payment_no ON tx_payment_logs (payment_no);
CREATE INDEX idx_tx_payment_logs_transaction_id ON tx_payment_logs (transaction_id);
CREATE INDEX idx_tx_payment_logs_action ON tx_payment_logs (action);
CREATE INDEX idx_tx_payment_logs_created_at ON tx_payment_logs (created_at);

COMMENT ON TABLE tx_payment_logs IS '支付渠道通信日志（对账与排障）';
COMMENT ON COLUMN tx_payment_logs.payment_id IS '关联 tx_payments.id';
COMMENT ON COLUMN tx_payment_logs.payment_no IS '支付单号（冗余）';
COMMENT ON COLUMN tx_payment_logs.channel IS '支付渠道';
COMMENT ON COLUMN tx_payment_logs.transaction_id IS '渠道交易号';
COMMENT ON COLUMN tx_payment_logs.action IS '操作类型：create-创建 pay-支付回调 refund-退款 refund_callback-退款回调 close-关闭';
COMMENT ON COLUMN tx_payment_logs.request_body IS '请求参数（渠道原始数据，用于对账排查）';
COMMENT ON COLUMN tx_payment_logs.response_body IS '响应结果（渠道原始数据）';
COMMENT ON COLUMN tx_payment_logs.status IS '操作结果状态';


CREATE TABLE tx_refunds (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    refund_no varchar(32) NOT NULL,
    payment_id bigint NOT NULL,
    payment_no varchar(32) NOT NULL,
    order_no varchar(32) NOT NULL,
    order_id bigint NOT NULL,
    merchant_id bigint NOT NULL DEFAULT 0,
    amount bigint NOT NULL,
    reason varchar(500) DEFAULT '',
    status varchar(20) NOT NULL DEFAULT 'pending',
    channel_refund_id varchar(128) DEFAULT NULL,
    failure_reason varchar(500) DEFAULT '',
    channel_response jsonb DEFAULT NULL,
    idempotency_key varchar(64) NOT NULL,
    applied_at timestamp(3) DEFAULT NULL,
    success_at timestamp(3) DEFAULT NULL,
    notify_at timestamp(3) DEFAULT NULL,
    created_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamp(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at timestamp(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE (refund_no),
    UNIQUE (channel_refund_id),
    UNIQUE (idempotency_key),
    CONSTRAINT chk_refund_amount CHECK (amount > 0)
);

CREATE INDEX idx_tx_refunds_payment_no ON tx_refunds (payment_no);
CREATE INDEX idx_tx_refunds_order_no ON tx_refunds (order_no);

CREATE TRIGGER trg_tx_refunds_updated_at
    BEFORE UPDATE ON tx_refunds
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

